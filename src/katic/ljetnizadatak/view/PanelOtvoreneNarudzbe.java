    private void ucitaj(){ 
        DefaultListModel<NarudzbaJelo> m = new DefaultListModel<NarudzbaJelo>();
        lista.setModel(m);       
        
        double suma = 0;
        for (NarudzbaJelo nj: narudzba.getNarudzbaJela()){
            m.addElement(nj);    
            suma += nj.getKolicina()*nj.getJelo().getCijena();
        }
        
        lblSuma.setText(String.format("%.2f kn", suma));
        lblAdresa.setText(narudzba.getAdresaDostave().toString());
        lblKontaktBroj.setText(narudzba.getKorisnik().getKontaktBroj());
    }    
    
    public void ucitajNarudzbu(Narudzba narudzba){
        this.narudzba = narudzba;
        ucitaj();
    }
    
    private void postaviAdresu(){
        List<AdresaDostave> adreseDostave = obradaAdresaDostave.getAdreseDostave(korisnik);
        if (adreseDostave==null){
            JOptionPane.showMessageDialog(this, "Dodajte barem jednu adresu preko izbornika moji podaci", "Nema spremljenih adresa", JOptionPane.WARNING_MESSAGE);         
        } else {
            AdresaDostave[] choices = adreseDostave.toArray(new AdresaDostave[adreseDostave.size()]);
            AdresaDostave adresa = new DialogListeAdresa().main(choices);
            if (adresa==null){
                return;
            }
            novaNarudzba.setAdresaDostave(adresa);
            lblAdresa.setText(adresa.toString());            
        }
    }
    
    private void dodajJelaUNarudzbu(){
        novaNarudzba.setNarudzbaJela(new ArrayList<>());
        for (NarudzbaJelo nj: narudzba.getNarudzbaJela()){
            NarudzbaJelo narudzbaJelo = new NarudzbaJelo();
            narudzbaJelo.setJelo(nj.getJelo());
            narudzbaJelo.setKolicina(nj.getKolicina());
            narudzbaJelo.setNarudzba(novaNarudzba);
            novaNarudzba.getNarudzbaJela().add(narudzbaJelo);
        }
    }
    
