    private void pnlDodajUKosaricuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlDodajUKosaricuMouseClicked
        try {
            obradaNarudzba.obrisiNeizvrseneNarudzbe(korisnik);            
            novaNarudzba = obradaNarudzba.spremi(novaNarudzba);
            dodajJelaUNarudzbu();
            novaNarudzba = obradaNarudzba.spremi(novaNarudzba);
            obradaNarudzbaJelo.spremi(novaNarudzba.getNarudzbaJela());
            korisnikListener.dodanaStaraNarudzba();
        } catch (Iznimka i){
            i.printStackTrace();
        }
    }//GEN-LAST:event_pnlDodajUKosaricuMouseClicked

