    private void ucitajKategorije(){
        DefaultListModel<KategorijaJela> m = new DefaultListModel<KategorijaJela>();
        lstKategorije.setModel(m);
        for (KategorijaJela k: obrada.getKategorijeJela()){
            m.addElement(k);
        }
    }
    
    private void ucitajJela(int sifraKategorije){
        DefaultListModel<Jelo> m = new DefaultListModel<Jelo>();
        lstJela.setModel(m);
        for (Jelo j: obrada.getJela(sifraRestorana, sifraKategorije)){
            m.addElement(j);
        }
    }
    
    private Image getScaledImage(Image srcImg, int w, int h){
        BufferedImage resizedImg = new BufferedImage(w, h, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2 = resizedImg.createGraphics();

        g2.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        g2.drawImage(srcImg, 0, 0, w, h, null);
        g2.dispose();

        return resizedImg;
    }

